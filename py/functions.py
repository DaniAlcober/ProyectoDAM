def suma(a, b):
    return a + b

def resta(a, b):
    return a - b

def multiplica(a, b):
    return a * b

def divide(a, b):
    return a / b

def cuentaString(str, char):
   return str.count(char)

def mayusculas(str):
    return str.upper()

def minusculas(str):
    return str.lower()

def mayusculasPrimera(str):
    return str.capitalize()



